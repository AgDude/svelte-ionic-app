Borat: Cultural Learnings of America for Make Benefit Glorious Nation of
Kazakhstan, more commonly known simply as Borat, is a 2006 satirical
mockumentary comedy film co-written and produced by British comedian Sacha Baron
Cohen who also plays the title character, Borat Sagdiyev, a fictitious Kazakh
journalist travelling through the United States recording real-life interactions
with Americans. The film was directed by Larry Charles and distributed by 20th
Century Fox. Much of the film features unscripted vignettes of Borat
interviewing and interacting with real-life Americans who believe he is a
foreigner with little or no understanding of American customs.[4] It is the
second of three films built around Baron Cohen's characters from Da Ali G Show
(2000–04): the first, Ali G Indahouse, was released in 2002, and featured a
cameo by Borat, and the third, Brüno, was released in 2009. The film is produced
by Baron Cohen's production company, Four By Two Productions. The film was very
well received, both critically and commercially; made for $18 million, it earned
$262 million worldwide. Baron Cohen won the Golden Globe Award for Best Actor in
a Motion Picture – Musical or Comedy, as Borat, while the film was nominated for
Best Motion Picture – Musical or Comedy in the same category. Borat was also
nominated for Best Adapted Screenplay at the 79th Academy Awards. Controversy
surrounded the film from two years prior to its release, and after the film's
release, some cast members spoke against, and even sued, its creators. It was
banned in almost all Arab countries, and the governments of Russia and
Kazakhstan discouraged cinemas from showing it. It was released on DVD in March
2007. At the behest of the Kazakh Ministry of Information, reporter Borat
Sagdiyev leaves his fictional hometown of Kuzec, Kazakhstan for the United
States, the "Greatest Country in the World", to make a documentary. He leaves
behind his wife Oksana and other inhabitants of his village. His companions are
his producer Azamat Bagatov and a pet chicken. In New York City, Borat sees an
episode of Baywatch on TV and immediately falls in love with Pamela Anderson's
character, C. J. Parker. While interviewing and mocking a panel of feminists, he
learns of the actress's name and her residence in California. Borat is initially
reluctant to pursue Pamela, because Oksana has threatened to attack him if he
cheats on her. However, he is then informed by telegram that she has been killed
by a bear. Delighted, he resolves to travel to California and make Pamela his
new wife. Azamat is afraid of flying because of the September 11, 2001, attacks,
which he believes were the work of Jews. Borat takes driving lessons and buys a
dilapidated ice-cream truck for the journey. During the trip, Borat meets gay
pride parade participants, and politicians Alan Keyes and Bob Barr. Borat is
also interviewed at a live local television station and proceeds to disrupt the
weather report. Visiting a rodeo in Virginia, Borat excites the crowd with a
jingoistic speech, but then sings a fictional Kazakh national anthem to the tune
of "The Star-Spangled Banner", receiving a strong negative reaction. Sad about
being hated by the rodeo crowd, Borat buys a Baywatch magazine at a yard sale to
change his fortune and continues gathering footage for his documentary. In
Georgia, Borat gets lost and asks for directions from a gang of African American
youths, who teach him how to speak and dress like them. Borat attempts to use
this approach to check into an expensive hotel, only to get turned away. Staying
at a bed-and-breakfast, Borat and Azamat are horrified to learn their hosts are
Jewish. The two escape after throwing money at two woodlice, believing they are
their hosts transformed. As they escape, Azamat insists that they return to New
York, as he believes that there are no Jews there. To appease Azamat, Borat
attempts to buy a handgun for protection, but is denied service for not being an
American citizen, so he buys an American black bear instead. An etiquette coach
suggests Borat attend a private dinner at an eating club in the South. During
the dinner, he unintentionally offends the other guests. When he lets Luenell,
an African-American prostitute, into the house and shows her to the table, they
both get kicked out. Borat befriends Luenell, and she invites him into a
relationship with her, but he tells her that he is in love with someone else.
Borat then visits an antique shop in Texas, where he clumsily breaks various
Confederate heritage items, almost bankrupting the documentary. At a hotel,
Borat, just out of the bath, catches Azamat, also naked, masturbating over the
Baywatch magazine. Enraged, Borat accidentally reveals that Pamela Anderson is
his real motive for travelling to California instead of staying in New York.
Azamat becomes livid at Borat's deception, and the situation escalates into a
nude brawl which spills out into the hallway, a crowded elevator, and then into
a packed convention ballroom. The next morning, Azamat abandons Borat, taking
his passport, all of their money, and the bear. Borat's truck runs out of gas,
and he begins to hitchhike to California. He is soon picked up by drunken
fraternity brothers from the University of South Carolina. On learning the
reason for his trip, they show him the Pam and Tommy sex disc, which reveals
that she is not a virgin, as he previously assumed she was. Heartbroken and
despondent, Borat burns the sex disc and the Baywatch magazine, as well as, by
mistake, his return ticket to Kazakhstan. He then considers killing and eating
his chicken, but cannot bring himself to do so and instead sets it free. The
next morning, Borat attends a United Pentecostal camp meeting in Arizona, at
which Republican U.S. Representative Chip Pickering and Mississippi Supreme
Court Chief Justice James W. Smith, Jr. are present. He regains his faith, and
forgives Pamela. He accompanies church members on a bus to Los Angeles and
disembarks to find Azamat dressed as Oliver Hardy, although Borat mistakes him
as Adolf Hitler. Borat forgives Azaamat, the two reconcile and Azamat tells
Borat where to find Pamela Anderson. Borat finally comes face-to-face with
Pamela at a book signing at a Virgin Megastore. After showing Pamela his
"traditional marriage sack", Borat pursues her throughout the store in an
attempt to abduct her until security guards intervene. Borat visits Luenell and
they return home to Kazakhstan together. Borat, Azamat and Luenell bring several
American customs and traditions back to Kuzec, including the apparent conversion
of the people to Christianity (the Kazakh version of which includes crucifixion
and torturing of Jews) and the introduction of computer-based technology, such
as iPods, laptop computers and a high-definition LCD television.
